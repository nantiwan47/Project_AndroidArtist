package com.example.project_androidartist.model;

public class Artist {
    String artist_name;
    String artist_image;
    int artist_debut;
    public String getArtist_name() {
        return artist_name;
    }

    public void setArtist_name(String artist_name) {
        this.artist_name = artist_name;
    }

    public String getArtist_image() {
        return artist_image;
    }

    public void setArtist_image(String artist_image) {
        this.artist_image = artist_image;
    }

    public int getArtist_debut() {
        return artist_debut;
    }

    public void setArtist_debut(int artist_debut) {
        this.artist_debut = artist_debut;
    }

}

